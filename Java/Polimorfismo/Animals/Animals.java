package Animals;

public class Animals {

	private String nome;
	private int idade;
	private String som;
	private String mov;
	private String imprimir;

	public String getNome() {
		return nome;
	}

	public void setNome(final String nome) {
		this.nome = nome;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(final int idade) {
		this.idade = idade;
	}

	public String getSom(){
		return som;
	}

	public void setSom(final String som) {
		this.som = som;
	}

	public String getMov() {
		return mov;
	}

	public void setMov(final String mov) {
		this.mov = mov;
	}

	public String getImprimir() {
		return imprimir;
	}

	public void setImprimir(String imprimir) {
		this.imprimir = imprimir;
	}

	
}
